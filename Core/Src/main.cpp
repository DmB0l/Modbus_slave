void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
	if (huart == &huart1) {
		start_time = HAL_GetTick();
		if (func_code == 0) {
			func_code = rx_buffer[1];
			if (func_code == 0x10 || func_code == 0x0F) {
				uint8_t ost_size = rx_buffer[6] - 1 + 2; // размер, который еще надо прочитать. количество байт в команде минус 1, потому что считали уже 8 байт, плюс 2 из-за crc
				rx_real_size = 8 + ost_size;
				HAL_UART_Receive_IT(&huart1, &rx_buffer[8], ost_size);
			} else {
				func_code = 0;
				mbs.write_command(rx_buffer, 8);
				mbs.execute();
				HAL_UART_Receive_IT(&huart1, rx_buffer, 8);
			}
		} else {
			func_code = 0;
			mbs.write_command(rx_buffer, rx_real_size);
			mbs.execute();
			rx_real_size = 0;
			HAL_UART_Receive_IT(&huart1, rx_buffer, 8);
		}
	}
}

